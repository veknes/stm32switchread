//Push Button pin read function with debouncing checking
void pushButtonRead(int pinStatus){
  int switchState;
  // If the switch changed, due to noise or pressing:
  if (pinStatus != lastButtonState) {
	  // reset the debouncing timer
	  lastDebounceTime = HAL_GetTick();
  }
  if ((HAL_GetTick() - lastDebounceTime) > debounceDelay) {
	  // whatever the reading is at, it's been there for longer than the debounce
	  // delay, so take it as the actual current state
	  if (pinStatus != buttonState) {
		buttonState = pinStatus;

		// only read the DIP switch if the push button state is HIGH
		if (buttonState) {
			switchState = (GPIOC->IDR & 0xF);
			selectedTimeDelay = (-58.3 * switchState) + 1000;
		}
	  }
  }
  // save the reading. Next time through the loop, it'll be the lastButtonState:
  lastButtonState = pinStatus;
}
